function displayimg(){
    var z = document.getElementById("item2");
    var y = document.getElementById("item1");
    if(y.type === 'item1'){
       y.style.display = "block";
       z.style.display = "none";
    }
    else{
       y.style.display = "none";
       z.style.display = "block";
    }
}
